package Entidades;

public class Arrays {

    private double[] array1;
    private double[] array2;

    public Arrays(double[] array1, double[] array2) {
        this.array1 = array1;
        this.array2 = array2;
    }

    public Arrays() {
    }

    public double[] getArray1() {
        return array1;
    }

    public double[] getArray2() {
        return array2;
    }

    public void setArray1(double[] array1) {
        this.array1 = array1;
    }

    public void setArray2(double[] array2) {
        this.array2 = array2;
    }
    
    
    
}
